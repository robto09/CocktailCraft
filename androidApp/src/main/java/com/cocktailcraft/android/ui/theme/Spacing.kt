package com.cocktailcraft.android.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Spacing scale for paddings, gaps, and spacers. Use these instead of raw
 * dp literals so layout rhythm stays consistent and adjustable in one place.
 * Component sizes (icons, images, touch targets) are not spacing — keep
 * those as explicit dp.
 */
object Spacing {
    val xs = 4.dp
    val sm = 8.dp
    val md = 12.dp
    val lg = 16.dp
    val xl = 20.dp
    val xxl = 24.dp
    val xxxl = 32.dp
}
